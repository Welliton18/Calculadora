<?php

class Calculadora {

    public static function soma($iNum1, $iNum2){
        return $iNum1 + $iNum2;
    }

    public static function subrtracao($iNum1, $iNum2){
        return $iNum1 - $iNum2;
    }

    public static function divisao($iNum1, $iNum2){
        return $iNum1 / $iNum2;
    }

    public static function multiplicacao($iNum1, $iNum2){
        return $iNum1 * $iNum2;
    }

}